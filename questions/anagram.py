word1 = "listen"
word2 = "silent"


# ==========================================================================================
# ------------------------- 1st method with using "not" ---------------------------------------
# ==========================================================================================
for i in word1:
    if i not in word2:
        print("False")
        break
else:
    print("True")

# ==========================================================================================
# ------------------------- 2nd method without using "not" ---------------------------------------
# ==========================================================================================
for i in word1:
    for j in word2:
        if i == j:
            break
    else:
        print("False")
        break
else:
    print("True")
    




